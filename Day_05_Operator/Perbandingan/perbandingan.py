# Operator perbandingan digunakan untuk membandingkan dua buah nilai
# ==, !=, >, <, <=, >=

a = 10
b = 3

print(a == b) # Sama dengan = False
print(a != b) # Tidak sama dengan = True
print(a >= b) # lebih dari sama dengan = True, 2 >= 2 akan True
print(a <= b) # Kurang dari sama dengan = False
print(a > b) # lebih dari = True, Note: 2 > 2 akan 
print(a < b) # Kurang dari = False