# Aritmatika digunakan untuk melakukan operasi dasar perhitungan
x = 10
y = 5

print("x + y =", x + y) # Addition
print("x - y =", x - y) # Subraction
print("x * y =", x * y) # Multiplication
print("x / y =", x / y) # Division
print("x // y =", x // y) # Floordivision
print("x % y =", x % y) # Modulus
print("x ** y =", x ** y) # Exponentation

print(x + y ** 2) # 35
print((x + y) ** 2) # 225
print((5 + 7) % 3 // 2 ** 2) # 0

print((-3) ** 2) # 9 -> (-3) * (-3) = 9
print( -3 ** 2) # -9 -> -(3 * 3) = 9

