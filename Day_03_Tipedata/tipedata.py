# Tipe Data Dasar
# INT, FLOAT, BOOL, AND STR

# INTEGER BILANGAN BULAT
x = 7 #Tipe data: integer
y = -3 #Tipe data: integer
z = 0 #Tipe data: integer
print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))

# FLOAT(FLOATING NUMBER) BILANGAN DESIMAL ATAU BILANGAN MENGGUNKAN TITIK.
p = 3.14 #FLOAT
l = -2.4 #FLOAT
# note: 2. = 2.0 atau 0. = 0.0
print(p)
print(type(p))
print(l)
print(type(l))

# BOOLEAN MENYATAKAN NILAI LOGIKA(TRUE/FALSE)
is_active = True   # is_active adalah boolean dengan nilai True
is_admin = False   # is_admin adalah boolean dengan nilai False
print(is_active)
print(is_admin)
print(type(is_active))
print(type(is_admin))

# STRING adalah kumpulan karakter yg diapit tanda kutip tunggal (' ') atau kutip ganda(" ")
name = "Alice"  # string dengan nilai "Alice"
greeting = 'Hello, World!'  #string
print(type(name))
print(type(greeting))


